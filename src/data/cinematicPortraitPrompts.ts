/**
 * Get a daily-rotating prompt with additional randomness
 * Uses day of year as base seed for daily variety
 */
export const getDailyCinematicPrompt = (): string => {
  // Calculate day of year
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);
  
  // Add random offset for variety within the same day
  const randomOffset = Math.floor(Math.random() * 30);
  
  // Calculate index with daily rotation
  const index = (dayOfYear * 7 + randomOffset) % cinematicPortraitPrompts.length;
  
  return cinematicPortraitPrompts[index];
};
